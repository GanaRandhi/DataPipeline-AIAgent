class ValidationTool:
    """
    Tool for validating pipeline configurations and generated code.
    
    Ensures quality and safety before execution.
    """
    
    @staticmethod
    @log_function_call
    def validate_pipeline_code(code: str) -> Dict[str, Any]:
        """
        Validate generated pipeline code.
        
        Args:
            code: Python code to validate
            
        Returns:
            Dict with validation results
        """
        issues = []
        warnings = []
        
        # Check for dangerous operations
        dangerous_patterns = [
            'os.system',
            'subprocess.call',
            'eval(',
            'exec(',
            '__import__'
        ]
        
        for pattern in dangerous_patterns:
            if pattern in code:
                issues.append(f"Dangerous pattern detected: {pattern}")
        
        # Check for best practices
        if 'try:' not in code:
            warnings.append("No error handling (try/except) found")
        
        if 'logging' not in code and 'logger' not in code:
            warnings.append("No logging configured")
        
        if 'def __init__' not in code:
            warnings.append("No initialization method found")
        
        result = {
            "valid": len(issues) == 0,
            "issues": issues,
            "warnings": warnings,
            "lines_of_code": len(code.split('\n'))
        }
        
        logger.info("Code validation complete", **result)
        return result


class DocumentationTool:
    """
    Tool for generating pipeline documentation.
    
    Creates README files and data lineage diagrams.
    """
    
    @staticmethod
    @log_function_call
    def generate_documentation(
        pipeline_name: str,
        description: str,
        sources: List[Dict[str, Any]],
        transformations: List[str],
        destination: Dict[str, Any]
    ) -> str:
        """
        Generate comprehensive pipeline documentation.
        
        Args:
            pipeline_name: Pipeline name
            description: Pipeline description
            sources: Data sources
            transformations: Transform steps
            destination: Data destination
            
        Returns:
            str: Markdown documentation
        """
        doc_template = '''# {pipeline_name}

{description}

## Overview

This pipeline processes data from {num_sources} source(s) and loads it to {dest_type}.

**Generated:** {timestamp}

## Data Sources

{sources_section}

## Transformations

{transformations_section}

## Destination

**Type:** {dest_type}
**Configuration:**
```json
{dest_config}
```

## Running the Pipeline

```bash
python {pipeline_name}.py
```

## Monitoring

Check logs at: `logs/{pipeline_name}.log`

## Data Quality

- Validation: Enabled
- Profiling: Enabled
- Error handling: Configured

## Maintenance

- Owner: Data Team
- Schedule: {schedule}
- Last updated: {timestamp}
'''
        
        # Build sources section
        sources_section = []
        for i, source in enumerate(sources, 1):
            sources_section.append(f"{i}. **{source.get('name', 'Source')}**")
            sources_section.append(f"   - Type: {source.get('type', 'unknown')}")
            if 'tables' in source:
                sources_section.append(f"   - Tables: {', '.join(source['tables'])}")
        
        # Build transformations section
        transformations_section = []
        for i, transform in enumerate(transformations, 1):
            transformations_section.append(f"{i}. {transform}")
        
        doc = doc_template.format(
            pipeline_name=pipeline_name,
            description=description,
            num_sources=len(sources),
            dest_type=destination.get('type', 'unknown'),
            sources_section='\n'.join(sources_section),
            transformations_section='\n'.join(transformations_section),
            dest_config=json.dumps(destination, indent=2),
            schedule="Not specified",
            timestamp=datetime.utcnow().isoformat()
        )
        
        logger.info("Documentation generated", pipeline=pipeline_name)
        return doc